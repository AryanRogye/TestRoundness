import CoreGraphics

extension CGRect {
    public func displaySizeWidth() -> String {
        return "\(width)"
    }
    public func displaySizeHeight() -> String {
        return "\(height)"
    }
    public func displaySize() -> String {
        return "\(width)x\(height)"
    }
}
